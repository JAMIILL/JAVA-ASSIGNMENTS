package ASSIGNMENTS;

public class Factorial {


		public static void main(String[] args) {
			int a=5;
			 
			int fact=1;
			for ( int i = 1; i <= a; i++) {
				  fact=fact*i;//fact =1*1=1
				              //FACT =1*2=2
				              //FACT =2*3=6
				              //FACT =6*4=24
				              //FACT =24*5=120
				                        
				
			}
			System.out.println(fact);
		}

	

}
